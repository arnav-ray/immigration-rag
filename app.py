# ── Input sanitisation helpers ──────────────────────────────────────────────────

def sanitise_query(raw: str) -> str | None:
    """
    Enforce length limit and strip leading/trailing whitespace.
    Returns None if the input exceeds MAX_QUERY_LEN (signals rejection).
    """
    stripped = raw.strip()
    if len(stripped) > config.MAX_QUERY_LEN:
        return None
    return stripped


def sanitise_history_turn(text: str) -> str:
    """Truncate a single history turn to MAX_HISTORY_TURN_LEN chars."""
    return text[: config.MAX_HISTORY_TURN_LEN]


def safe_url(href: str) -> str | None:
    """Return href only if it is a valid https:// URL, else None."""
    try:
        parsed = urlparse(href)
        if parsed.scheme == "https" and parsed.netloc:
            return href
    except Exception:
        pass
    return None


# --- HELPER: extract explicit § references from user query ---
def extract_explicit_sections(query: str) -> list:
    """Return a list of § numbers explicitly named in the query (e.g. ['18b', '4a'])."""
    return re.findall(r'§\s*(\d+[a-z]*)', query, re.IGNORECASE)


# --- INITIALIZE AI (Cached so it loads instantly) ---
@st.cache_resource
def init_rag():
    # Setup LLM and Embedding Model
    Settings.llm = Ollama(
        model=config.LLM_MODEL,
        request_timeout=config.LLM_REQUEST_TIMEOUT,
        additional_kwargs={"num_ctx": config.LLM_NUM_CTX},
    )
    Settings.embed_model = OllamaEmbedding(
        model_name=config.EMBED_MODEL,
        text_instruction="search_document: ",
        query_instruction="search_query: ",
    )

    # Load the database from the hard drive
    storage_context = StorageContext.from_defaults(persist_dir=config.PERSIST_DIR)
    index = load_index_from_storage(storage_context)

    reranker = CrossEncoder(
        config.RERANKER_MODEL,
        max_length=config.RERANKER_MAX_LEN,
    )

    return reranker, index


reranker, index = init_rag()

